# ===========================================================================
# VariableCollector
# ===========================================================================
class VariableCollector:
    """
    観光需要予測用の主要変数をハードコード/APIから収集し、
    パネルデータ（年次→月次補間、標準化）として出力する。
    """

    def __init__(self) -> None:
        self._raw_data: Dict[str, Dict[str, Dict[int, float]]] = dict(_VARIABLE_DATA)

    # -----------------------------------------------------------------------
    # collect_panel_data — メインエントリポイント
    # -----------------------------------------------------------------------
    def collect_panel_data(
        self,
        countries: Optional[List[str]] = None,
        years: Optional[List[int]] = None,
        monthly: bool = True,
        standardize: bool = True,
    ) -> "pd.DataFrame":
        """
        パネルデータを収集・前処理する。

        Args:
            countries: 対象国リスト (None=全11カ国)
            years: 対象年リスト (None=2015-2024)
            monthly: True→cubic spline で月次補間
            standardize: True→変数を標準化 (平均0, 標準偏差1)

        Returns:
            pd.DataFrame: columns=[country, year(/year_month), var1, var2, ...]
        """
        if not PD_AVAILABLE:
            raise ImportError("pandas が必要です: pip install pandas")

        countries = countries or TARGET_COUNTRIES
        years = years or TARGET_YEARS

        # 1) 年次パネルを組み立て
        records = []
        for c in countries:
            for y in years:
                row = {"country": c, "year": y}
                for var_name, data_dict in self._raw_data.items():
                    country_data = data_dict.get(c, {})
                    row[var_name] = country_data.get(y, np.nan)
                records.append(row)

        df = pd.DataFrame(records)

        # 2) 欠損処理: backward fill → forward fill (国別)
        var_cols = [c for c in df.columns if c not in ("country", "year")]
        df = df.sort_values(["country", "year"])
        for col in var_cols:
            df[col] = df.groupby("country")[col].transform(
                lambda s: s.bfill().ffill()
            )

        # 3) 月次補間 (cubic spline)
        if monthly:
            df = self._interpolate_monthly(df, var_cols)

        # 4) 標準化
        if standardize:
            target_cols = [c for c in df.columns
                           if c not in ("country", "year", "year_month", "month")]
            for col in target_cols:
                mean_val = df[col].mean()
                std_val = df[col].std()
                if std_val > 0:
                    df[col] = (df[col] - mean_val) / std_val

        logger.info(
            "パネルデータ収集完了: %d行 × %d列 (%d カ国, %s)",
            len(df), len(df.columns), len(countries),
            "月次" if monthly else "年次",
        )
        return df

    # -----------------------------------------------------------------------
    # _interpolate_monthly — 年次→月次 cubic spline 補間
    # -----------------------------------------------------------------------
    def _interpolate_monthly(
        self,
        df: "pd.DataFrame",
        var_cols: List[str],
    ) -> "pd.DataFrame":
        """年次データを月次に cubic spline 補間する。"""
        monthly_records = []

        for country in df["country"].unique():
            cdf = df[df["country"] == country].sort_values("year")
            years = cdf["year"].values
            n_years = len(years)

            if n_years < 2:
                # 補間不能 → 12ヶ月同値コピー
                for _, row in cdf.iterrows():
                    for m in range(1, 13):
                        rec = {
                            "country": country,
                            "year": int(row["year"]),
                            "month": m,
                            "year_month": f"{int(row['year'])}-{m:02d}",
                        }
                        for col in var_cols:
                            rec[col] = row[col]
                        monthly_records.append(rec)
                continue

            # 年中央（7月=0.5）を基準点にする
            year_points = years.astype(float) + 0.5

            for col in var_cols:
                vals = cdf[col].values
                if np.all(np.isnan(vals)):
                    continue

                # cubic spline 補間
                if SCIPY_AVAILABLE and n_years >= 4:
                    cs = CubicSpline(year_points, vals, bc_type="natural")
                else:
                    cs = None

                for i, yr in enumerate(years):
                    for m in range(1, 13):
                        t = float(yr) + (m - 0.5) / 12.0
                        key = (country, yr, m)

                        # monthly_records に既存エントリがなければ作成
                        rec = None
                        for r in monthly_records:
                            if (r["country"] == country
                                    and r["year"] == int(yr)
                                    and r["month"] == m):
                                rec = r
                                break
                        if rec is None:
                            rec = {
                                "country": country,
                                "year": int(yr),
                                "month": m,
                                "year_month": f"{int(yr)}-{m:02d}",
                            }
                            monthly_records.append(rec)

                        if cs is not None:
                            rec[col] = float(cs(t))
                        else:
                            # 線形補間フォールバック
                            rec[col] = float(np.interp(t, year_points, vals))

        return pd.DataFrame(monthly_records)

    # -----------------------------------------------------------------------
    # get_variable_list — 変数一覧
    # -----------------------------------------------------------------------
    def get_variable_list(self) -> List[str]:
        """収集可能な変数名のリストを返す。"""
        return list(VARIABLE_NAMES)

    # -----------------------------------------------------------------------
    # get_raw_data — 特定変数の生データ
    # -----------------------------------------------------------------------
